public class DataTypes_02 {


    //https://en.wikibooks.org/wiki/Java_Programming/Primitive_Types
    //https://docs.oracle.com/javase/tutorial/java/nutsandbolts/datatypes.html

    // 1- Datatype variableName = value
    // 2- Datatype variableName;
    // variableName = value

    public static void main(String[] args) {

        byte myByte = 100;
        System.out.println("This is myByte" + myByte);

        byte myByte2;
        myByte2 = 123;
        System.out.println("This is myByte2" + myByte2);

        boolean myBoolean = false;
        System.out.println(false);

        char myChar = '`';
        System.out.println(myChar);

        //how to convert (casting) char to ascii character
        char c = '$';
        System.out.println(c);

        float ascii = c;
        System.out.println(ascii);



        //non-primitive




    }




}
